                <h2 id="pros-and-cons" class="scroll-mt-24 text-2xl sm:text-3xl font-bold mt-14 mb-4 text-balance">Pros and cons of dichotomous questions</h2>

                <p class="mb-6">Used in the right place, dichotomous questions are a survey designer's best friend. Used in the wrong place, they're the format that quietly destroys the most data.</p>

                <div class="grid grid-cols-1 sm:grid-cols-2 gap-5 mb-8">
                    <div class="rounded-xl border-2 border-black shadow-[3px_3px_0_0_#000] bg-emerald-50 p-6">
                        <p class="text-xs font-bold uppercase tracking-wider text-emerald-700 mb-3">&#10004; Strengths</p>
                        <ul class="space-y-2 m-0 list-disc pl-5 marker:text-emerald-700 text-base text-gray-800">
                            <li><strong>Fast to answer.</strong> Two options, one tap.</li>
                            <li><strong>Easy to analyze.</strong> Every result reduces to "% yes."</li>
                            <li><strong>High response rate.</strong> Lowest friction format possible.</li>
                            <li><strong>Clean branching.</strong> Perfect for conditional logic and skip patterns.</li>
                            <li><strong>Reliable for binary facts.</strong> No ambiguity about what the answer means.</li>
                        </ul>
                    </div>
                    <div class="rounded-xl border-2 border-black shadow-[3px_3px_0_0_#000] bg-red-50 p-6">
                        <p class="text-xs font-bold uppercase tracking-wider text-red-700 mb-3">&#10006; Weaknesses</p>
                        <ul class="space-y-2 m-0 list-disc pl-5 marker:text-red-700 text-base text-gray-800">
                            <li><strong>Loses nuance.</strong> Mixed feelings collapse into a misleading yes or no.</li>
                            <li><strong>Forces a side.</strong> No "maybe" option pushes people off the fence.</li>
                            <li><strong>Misses gradation.</strong> Can't tell strong yes from weak yes.</li>
                            <li><strong>Can introduce bias.</strong> If neither option fits, respondents pick the less wrong one.</li>
                            <li><strong>Hard to trend.</strong> Limited statistical depth compared to scaled data.</li>
                        </ul>
                    </div>
                </div>

                <h2 id="how-to-analyze" class="scroll-mt-24 text-2xl sm:text-3xl font-bold mt-14 mb-4 text-balance">How to analyze and score dichotomous question data</h2>

                <p class="mb-6">Binary data is the easiest survey output to work with, but the analysis still needs to be done deliberately. Three common patterns:</p>

                <h3 class="text-xl sm:text-2xl font-semibold mt-8 mb-3">1. Calculate the percentage</h3>
                <p class="mb-6">The default metric for any dichotomous question is the share of respondents who picked each option. Tally the "yes" responses, divide by the total number of valid responses, and report a percentage. If 642 out of 900 respondents said yes, that's a 71.3% yes rate.</p>

                <h3 class="text-xl sm:text-2xl font-semibold mt-8 mb-3">2. Cross-tabulate against segments</h3>
                <p class="mb-6">The single percentage is rarely the interesting number; the difference between segments is. Break the "% yes" out by audience cut (new vs returning customers, free vs paid, region, plan tier). A 10-point gap between any two segments is usually where the actual signal lives.</p>

                <h3 class="text-xl sm:text-2xl font-semibold mt-8 mb-3">3. Pair binary with a follow-up</h3>
                <p class="mb-6">A dichotomous question on its own tells you <em>what</em>. The follow-up tells you <em>why</em>. Branch the survey so anyone who answered yes (or no) gets a single open-ended question after. That's where the qualitative depth comes back into binary data.</p>

                <p class="mb-6">For trend reporting, plot "% yes" over time the same way you'd plot any other rate metric. Most survey tools (Youform included) export the raw responses as CSV so you can wire them straight into your dashboard of choice.</p>

                <h2 id="vs-alternatives" class="scroll-mt-24 text-2xl sm:text-3xl font-bold mt-14 mb-4 text-balance">Dichotomous vs other question types</h2>

                <p class="mb-6">Picking the right question type comes down to matching the format to the shape of the answer. Here's how dichotomous compares to its closest cousins.</p>

                <div class="space-y-6 mb-8">
                    <div class="rounded-xl border border-gray-200 p-6 bg-white">
                        <h3 class="text-lg sm:text-xl font-bold mb-2 mt-0">Dichotomous vs multiple choice</h3>
                        <p class="text-base text-gray-700 mb-3 m-0">Multiple choice has three or more options. Reach for it when the underlying choice isn't binary. "Which device do you use most often?" (iOS / Android / Web) is a three-option fact, not a yes/no opinion.</p>
                    </div>

                    <div class="rounded-xl border border-gray-200 p-6 bg-white">
                        <h3 class="text-lg sm:text-xl font-bold mb-2 mt-0">Dichotomous vs Likert scale</h3>
                        <p class="text-base text-gray-700 mb-3 m-0">Likert scales (typically 5 or 7 points: strongly disagree &rarr; strongly agree) capture intensity. If you want to know <em>how much</em> the respondent agrees, switch to Likert. Yes/no flattens "kind of agree" into the wrong bucket.</p>
                    </div>

                    <div class="rounded-xl border border-gray-200 p-6 bg-white">
                        <h3 class="text-lg sm:text-xl font-bold mb-2 mt-0">Dichotomous vs rating scale</h3>
                        <p class="text-base text-gray-700 mb-3 m-0">Rating scales (1&ndash;5, 1&ndash;10, NPS) measure magnitude. Use them when the metric needs to be benchmarked or trended over time. Binary data can't be averaged into a trend with the same precision.</p>
                    </div>

                    <div class="rounded-xl border border-gray-200 p-6 bg-white">
                        <h3 class="text-lg sm:text-xl font-bold mb-2 mt-0">Dichotomous vs open-ended</h3>
                        <p class="text-base text-gray-700 mb-3 m-0">Open-ended questions capture qualitative depth that binary can't reach. The smart pattern is to combine them: a dichotomous question to filter or measure, followed by an open-ended "why" for the people whose answer interests you most.</p>
                    </div>
                </div>

                <h2 id="best-practices" class="scroll-mt-24 text-2xl sm:text-3xl font-bold mt-14 mb-4 text-balance">Best practices for writing dichotomous questions</h2>

                <p class="mb-6">A dichotomous question is only as good as its wording. Five rules that separate clean binary questions from broken ones:</p>

                <ol class="space-y-3 mb-6 list-decimal pl-6 marker:text-coral marker:font-semibold">
                    <li><strong>Make the two options mutually exclusive and exhaustive.</strong> Every respondent must fit into exactly one. If someone could be "kind of both" or "neither," the format is wrong for this question.</li>
                    <li><strong>Stay neutral.</strong> Don't bias the wording toward one option. "Did our excellent support resolve your issue? (Yes / No)" is leading; "Did support resolve your issue? (Yes / No)" isn't.</li>
                    <li><strong>Ask about one thing at a time.</strong> If you find yourself writing "and" or "or" in the stem, you've drifted into a <a href="/blog/double-barreled-questions" class="text-coral hover:text-coral/80 underline">double-barreled question</a>, the worst format mistake in survey design.</li>
                    <li><strong>Consider a "not sure" or "prefer not to say" option where it's honest.</strong> Forcing a binary answer when respondents genuinely don't know turns guesses into "data." It's not always wrong to add a third option, just know that the question stops being technically dichotomous.</li>
                    <li><strong>Pair it with a follow-up.</strong> Binary alone tells you what; an open-ended "why" or a Likert follow-up tells you how strongly. Combining the two is almost always better than either alone.</li>
                </ol>

                <h2 id="checklist" class="scroll-mt-24 text-2xl sm:text-3xl font-bold mt-14 mb-4 text-balance">A pre-send checklist before your next survey</h2>

                <div class="rounded-xl border-2 border-black shadow-[3px_3px_0_0_#000] bg-pale-violet/30 p-6 sm:p-7 mb-6">
                    <ul class="space-y-3 m-0">
                        <li class="flex items-start gap-3">
                            <span class="mt-1 inline-flex shrink-0 size-5 items-center justify-center rounded border-2 border-black bg-white text-xs font-bold">&#10004;</span>
                            <span>Each dichotomous question describes a genuinely binary reality, not a continuous one.</span>
                        </li>
                        <li class="flex items-start gap-3">
                            <span class="mt-1 inline-flex shrink-0 size-5 items-center justify-center rounded border-2 border-black bg-white text-xs font-bold">&#10004;</span>
                            <span>The two options are mutually exclusive and cover every respondent.</span>
                        </li>
                        <li class="flex items-start gap-3">
                            <span class="mt-1 inline-flex shrink-0 size-5 items-center justify-center rounded border-2 border-black bg-white text-xs font-bold">&#10004;</span>
                            <span>The wording is neutral. No adjectives nudge respondents toward one side.</span>
                        </li>
                        <li class="flex items-start gap-3">
                            <span class="mt-1 inline-flex shrink-0 size-5 items-center justify-center rounded border-2 border-black bg-white text-xs font-bold">&#10004;</span>
                            <span>Sensitive questions get a "prefer not to say" if honesty depends on it.</span>
                        </li>
                        <li class="flex items-start gap-3">
                            <span class="mt-1 inline-flex shrink-0 size-5 items-center justify-center rounded border-2 border-black bg-white text-xs font-bold">&#10004;</span>
                            <span>Every "yes" with downstream value has a follow-up that captures depth.</span>
                        </li>
                    </ul>
                </div>

                <p class="mb-6">If you can check all five, your binary questions are doing exactly what they're best at, producing decisive, clean, and trustworthy data.</p>

                <h2 id="faq" class="scroll-mt-24 text-2xl sm:text-3xl font-bold mt-14 mb-6 text-balance">Frequently asked questions</h2>

                <div class="space-y-4 mb-8">
                    <details class="group rounded-xl border border-gray-200 bg-white open:border-coral transition-colors">
                        <summary class="cursor-pointer list-none px-5 py-4 sm:px-6 sm:py-5 flex justify-between items-start gap-4 font-semibold text-base sm:text-lg">
                            <span>What is a dichotomous question?</span>
                            <span class="shrink-0 mt-1 transition-transform group-open:rotate-45 text-coral text-2xl leading-none">+</span>
                        </summary>
                        <div class="px-5 pb-5 sm:px-6 sm:pb-6 text-gray-700">
                            A dichotomous question is a closed-ended survey question that offers exactly two mutually exclusive answer options, most commonly yes/no, true/false, or agree/disagree. The respondent picks one, and the response is binary.
                        </div>
                    </details>

                    <details class="group rounded-xl border border-gray-200 bg-white open:border-coral transition-colors">
                        <summary class="cursor-pointer list-none px-5 py-4 sm:px-6 sm:py-5 flex justify-between items-start gap-4 font-semibold text-base sm:text-lg">
                            <span>What is an example of a dichotomous question?</span>
                            <span class="shrink-0 mt-1 transition-transform group-open:rotate-45 text-coral text-2xl leading-none">+</span>
                        </summary>
                        <div class="px-5 pb-5 sm:px-6 sm:pb-6 text-gray-700">
                            "Have you purchased from us in the last 30 days? (Yes / No)" is a classic dichotomous question. Other examples: "Are you currently employed?", "Do you own a smartphone?", "Did you find what you were looking for today?"
                        </div>
                    </details>

                    <details class="group rounded-xl border border-gray-200 bg-white open:border-coral transition-colors">
                        <summary class="cursor-pointer list-none px-5 py-4 sm:px-6 sm:py-5 flex justify-between items-start gap-4 font-semibold text-base sm:text-lg">
                            <span>When should you use a dichotomous question?</span>
                            <span class="shrink-0 mt-1 transition-transform group-open:rotate-45 text-coral text-2xl leading-none">+</span>
                        </summary>
                        <div class="px-5 pb-5 sm:px-6 sm:pb-6 text-gray-700">
                            Use a dichotomous question when (1) the answer is genuinely binary in reality (employed vs not, eligible vs not), (2) you're screening or qualifying respondents at the start of a survey, (3) you need to branch the survey based on the answer, or (4) you want the highest possible response rate on a sensitive question.
                        </div>
                    </details>

                    <details class="group rounded-xl border border-gray-200 bg-white open:border-coral transition-colors">
                        <summary class="cursor-pointer list-none px-5 py-4 sm:px-6 sm:py-5 flex justify-between items-start gap-4 font-semibold text-base sm:text-lg">
                            <span>What is the difference between a dichotomous question and a multiple choice question?</span>
                            <span class="shrink-0 mt-1 transition-transform group-open:rotate-45 text-coral text-2xl leading-none">+</span>
                        </summary>
                        <div class="px-5 pb-5 sm:px-6 sm:pb-6 text-gray-700">
                            A dichotomous question has exactly two options. A multiple choice question has three or more. Dichotomous is faster to answer and easier to analyze, but loses nuance. Multiple choice captures gradations of opinion at the cost of speed and clarity.
                        </div>
                    </details>

                    <details class="group rounded-xl border border-gray-200 bg-white open:border-coral transition-colors">
                        <summary class="cursor-pointer list-none px-5 py-4 sm:px-6 sm:py-5 flex justify-between items-start gap-4 font-semibold text-base sm:text-lg">
                            <span>Are dichotomous questions reliable?</span>
                            <span class="shrink-0 mt-1 transition-transform group-open:rotate-45 text-coral text-2xl leading-none">+</span>
                        </summary>
                        <div class="px-5 pb-5 sm:px-6 sm:pb-6 text-gray-700">
                            They're highly reliable for genuinely binary attributes (did the user complete the action? yes/no) and unreliable when forced onto attributes with natural gradations (satisfaction, agreement, frequency). Asking "Are you satisfied? (Yes/No)" hides every shade of mixed feeling. Use a Likert scale instead.
                        </div>
                    </details>

                    <details class="group rounded-xl border border-gray-200 bg-white open:border-coral transition-colors">
                        <summary class="cursor-pointer list-none px-5 py-4 sm:px-6 sm:py-5 flex justify-between items-start gap-4 font-semibold text-base sm:text-lg">
                            <span>What is the disadvantage of a dichotomous question?</span>
                            <span class="shrink-0 mt-1 transition-transform group-open:rotate-45 text-coral text-2xl leading-none">+</span>
                        </summary>
                        <div class="px-5 pb-5 sm:px-6 sm:pb-6 text-gray-700">
                            The biggest disadvantage is loss of nuance. Forcing a binary answer where reality is continuous (e.g., satisfaction, agreement, frequency) flattens real opinions into noise. It can also push respondents to pick whichever side is closest, even when neither fits, producing data that looks decisive but isn't.
                        </div>
                    </details>

                    <details class="group rounded-xl border border-gray-200 bg-white open:border-coral transition-colors">
                        <summary class="cursor-pointer list-none px-5 py-4 sm:px-6 sm:py-5 flex justify-between items-start gap-4 font-semibold text-base sm:text-lg">
                            <span>Is a yes/no question a dichotomous question?</span>
                            <span class="shrink-0 mt-1 transition-transform group-open:rotate-45 text-coral text-2xl leading-none">+</span>
                        </summary>
                        <div class="px-5 pb-5 sm:px-6 sm:pb-6 text-gray-700">
                            Yes, a yes/no question is the most common form of dichotomous question. True/false, agree/disagree, present/absent, and any two-option pairing also qualify, as long as the two options are mutually exclusive and exhaustive.
                        </div>
                    </details>
                </div>

