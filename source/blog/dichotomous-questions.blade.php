                <p class="mb-6">Every survey designer eventually faces the same choice: do you ask a simple <strong>yes/no</strong> question and get a clean answer, or do you ask a scaled question and capture the nuance?</p>

                <p class="mb-6">The dichotomous question, any question with exactly two mutually exclusive options, is the workhorse of survey design. It's fast, easy to analyze, and incredibly reliable when used correctly. It's also one of the most overused formats, applied to questions that don't have a binary answer in real life.</p>

                <p class="mb-6">This guide covers what dichotomous questions are, when they genuinely beat the alternatives, 25 real examples across customer research, employee, health, education and political surveys, the pros and cons, and exactly when to reach for a Likert scale or multiple choice instead.</p>

                <h2 id="what-are-dichotomous-questions" class="scroll-mt-24 text-2xl sm:text-3xl font-bold mt-14 mb-4 text-balance">What are dichotomous questions?</h2>

                <p class="mb-5">A dichotomous question is a <strong>closed-ended survey question that offers exactly two answer options</strong>. The two options must be mutually exclusive (you can't pick both) and collectively exhaustive (every respondent fits one of them).</p>

                <p class="mb-5">The word comes from the Greek <em>dichotomia</em>, literally "cutting in two." Every respondent gets cut into one of two buckets.</p>

                <p class="mb-6">The most common forms are:</p>

                <ul class="space-y-2 mb-6 list-disc pl-6 marker:text-coral">
                    <li><strong>Yes / No</strong>: "Have you used our product before?"</li>
                    <li><strong>True / False</strong>: used heavily in quizzes and knowledge tests</li>
                    <li><strong>Agree / Disagree</strong>: sometimes used in attitude research</li>
                    <li><strong>Fair / Unfair</strong>: common in policy and ethics research</li>
                    <li><strong>Present / Absent</strong>: medical or observational data</li>
                    <li><strong>This / That</strong>: "Coffee or tea?", "iOS or Android?"</li>
                </ul>

                <!-- Example card -->
                <div class="my-7 rounded-xl border-2 border-black shadow-[3px_3px_0_0_#000] overflow-hidden bg-white">
