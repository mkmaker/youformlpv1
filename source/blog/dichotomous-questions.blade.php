                <p class="mb-6">Dichotomous questions sit in a specific spot in the survey design toolkit: simpler than multiple choice, simpler than Likert, more decisive than open-ended. The trick is knowing when "simpler" is the right call.</p>

                <h2 id="when-to-use" class="scroll-mt-24 text-2xl sm:text-3xl font-bold mt-14 mb-4 text-balance">When to use a dichotomous question</h2>

                <p class="mb-6">There are four situations where a dichotomous question is clearly the right tool:</p>

                <ol class="space-y-3 mb-6 list-decimal pl-6 marker:text-coral marker:font-semibold">
                    <li><strong>The underlying reality is binary.</strong> "Are you currently employed?", "Do you own a car?", "Are you over 18?" These all have a real-world yes-or-no answer. No scale will improve them.</li>
                    <li><strong>You're screening or qualifying respondents.</strong> A first question like "Have you used our product in the last 90 days?" filters who continues. The branching depends on a binary answer.</li>
                    <li><strong>You're branching the survey.</strong> "Did you contact support?" routes one group to follow-up questions and skips them for everyone else. Conditional logic in form builders runs on binary signals.</li>
                    <li><strong>You want the highest response rate on a sensitive question.</strong> Binary options are the lowest-friction format, so people are likeliest to answer them honestly, especially when the topic is uncomfortable.</li>
                </ol>

                <p class="mb-6">And three situations where you should not use one:</p>

                <ul class="space-y-3 mb-6 list-disc pl-6 marker:text-coral">
                    <li><strong>The attribute is continuous.</strong> Satisfaction, agreement, frequency, intent, trust: these all have natural gradations. A 5- or 7-point Likert scale captures the gradient; a yes/no flattens it.</li>
                    <li><strong>The respondent has mixed feelings.</strong> If the honest answer is "kind of," forcing yes or no produces noise, not data.</li>
                    <li><strong>You actually want gradation in your reporting.</strong> If your dashboards or KPIs require averages, distributions, or trend comparisons, you need a scale that produces them. Binary data limits you to "% yes."</li>
                </ul>

